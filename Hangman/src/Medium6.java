    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String input = jTextField1.getText().trim();
        if (input.length() != 1 || !Character.isLetter(input.charAt(0))) {
            JOptionPane.showMessageDialog(this, "Please enter a valid single letter.");
            return;
        }
        char guess = input.toLowerCase().charAt(0);
        boolean result = hangmanm6.makeGuess(guess);
        if (result) {
            JOptionPane.showMessageDialog(this, "Correct guess!");
        } else {
            JOptionPane.showMessageDialog(this, "Incorrect guess!");
        }
        updateUI();
        jTextField1.setText("");
        if (hangmanm6.isGameFinished()) {
            if (hangmanm6.isGameWon()) {
                JOptionPane.showMessageDialog(this, "Congratulations! You've guessed the word: " + hangmanm6.getWordToGuess());
            } else {
                JOptionPane.showMessageDialog(this, "Sorry, you've run out of tries. The word was: " + hangmanm6.getWordToGuess());
            }
            hangmanm6.reset();
            updateUI();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

