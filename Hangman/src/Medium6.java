    private void updateUI() {
        jTextArea1.setText("Current guess: " + hangmanm6.getCurrentGuess() + "\n");
        jTextArea1.append("Tries left: " + hangmanm6.getTriesLeft() + "\n");
        jTextArea1.append("Incorrect letters: ");
        for (int i = 0; i < hangmanm6.getIncorrectGuess().length; i++) {
            if (hangmanm6.getIncorrectGuess()[i] != 0) {
                jTextArea1.append(hangmanm6.getIncorrectGuess()[i] + " ");
            }
        }
        jTextArea1.append("\n"); // Add a newline after displaying the incorrect guesses
    }
